a = 4
b = 3


# Arithmetic operators

print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)
print(a ** b)
print(a // b)

# Comparison operators

print(a > b)
print(a < b)
print(a == b)
print(a != b)
print (a >= b)
print(a <= b)

print (a > b and a != b)
print (a > b or a != b) 

#Logical operators
print (a > b and a != b)
print (a > b or a != b)
print (not(a > b and a != b))

#Bitwise operators
print(a & b)
print(a | b)
print(a ^ b)
print(~a)
print(a << 2)
print(a >> 2)

